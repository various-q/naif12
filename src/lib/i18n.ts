export const useTranslation = (language: Language = 'ar') => {
  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  const getDirection = (): 'ltr' | 'rtl' => {
    return language === 'ar' ? 'rtl' : 'ltr';
  };

  return { t, getDirection };
};